// Current language
let currentLang = localStorage.getItem('lang') || 'th';

function t(key) {
  return TRANSLATIONS[currentLang][key] || TRANSLATIONS['th'][key] || key;
}

const _langChangeCallbacks = [];
function onLangChange(fn) { _langChangeCallbacks.push(fn); }

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('lang', lang);
  document.documentElement.lang = lang === 'th' ? 'th' : 'en';
  updateAllText();
  _langChangeCallbacks.forEach(fn => { try { fn(lang); } catch {} });
}

function updateAllText() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = t(key);
    } else {
      el.textContent = t(key);
    }
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.getAttribute('data-i18n-title'));
  });
}

// Initialize on load
document.addEventListener('DOMContentLoaded', () => {
  document.documentElement.lang = currentLang === 'th' ? 'th' : 'en';
});
